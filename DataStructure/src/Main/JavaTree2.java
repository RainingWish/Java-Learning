//Here are several diffent type of binary Tree

/*
 * 	Full Binary Tree A Binary Tree is full if every node has 0 or 2 children.
 *  Following are examples of a full binary tree. 
 *  We can also say a full binary tree is a binary tree 
 *  in which all nodes except leaves have two children.
 * 
 * ie:
 *  		 18
           /    \   
         15     20    
        /  \       
      40    50   
    /   \
   30   50
   
 * In a Full Binary, number of leaf nodes is number of internal nodes plus 1
 *      L = I + 1
 *	Where L = Number of leaf nodes, I = Number of internal nodes
 *  
 */

/*
 * 	Complete Binary Tree: A Binary Tree is complete Binary Tree
 *  if all levels are completely filled except 
 *  possibly the last level and the last level has all keys 
 *  as left as possible
 *  
 *  ie:
 *   		   18
           /       \  
         15         30  
        /  \        /  \
      40    50    100   40
     /  \   /
    8   7  9 
 *  
 */


/*
 * Perfect Binary Tree A Binary tree is Perfect Binary Tree 
 * in which all internal nodes have two children
 *  and all leaves are at the same level.
 *
 *ie:
 *			   18
           /       \  
         15         30  
        /  \        /  \
      40    50    100   40
 *
 *
 *A Perfect Binary Tree of height h 
 *(where height is the number of nodes on the path from the root to leaf) 
 *has 2^h ¨C 1 node.
 *
 */

